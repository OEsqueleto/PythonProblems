info = input('Escreva algo aqui zé:')

print('Qual é o tipo?',type(info))
print('É alfa númerico?',info.isalnum())
print('É alfabético?',info.isalpha())
print('É ASCII?',info.isascii())
print('É decimal?',info.isdecimal())
print('É digito?',info.isdigit())
print('É uppercase?',info.isupper())
print('É capitalizada?',info.istitle())
print('É lowercase?',info.islower())
print('É vazio?',info.isspace())

